void CursorRight(){
	GPIOD->ODR &= ~(0xff);//remise a 0
	GPIOE->ODR &= ~(0xff);//remise a 0
	GPIOD->ODR |= BIT2+BIT3+BIT4; //commande de shift de curseur a droite
	for (int k = 0; k < 100000; k++){}
	GPIOE->ODR |= BIT2;// enable 1 logique
	for (int k = 0; k < 100000; k++){}
	GPIOE->ODR &= ~(0xff);// enable 0 logique et remise a 0
}


void CursorLeft(){
	GPIOD->ODR &= ~(0xff);//remise a 0
	GPIOE->ODR &= ~(0xff);
	GPIOD->ODR |= BIT4;
	for (int k = 0; k < 10000; k++){}
	GPIOE->ODR |= BIT2;// enable 1 logique
	for (int k = 0; k < 10000; k++){}
	GPIOE->ODR &= ~(0xff);// enable 0 logique et remise a 0
}

