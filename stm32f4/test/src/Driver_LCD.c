void nouvEcriture(char n) {
	if(cmpt == 0){// si on appui sur une touche
		c = n;
	} else if (n != c) {// si on appui sur deux touches (deuxieme fois qu'on utilise nouvEcriture pour une autre touche)
	c2 = n;
	}
	NouvelleEcriture = 1;
	cmpt++;
}

